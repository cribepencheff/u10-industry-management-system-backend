export const success = (msg) => `\x1b[32m${msg}\x1b[0m`; // Green
export const error = (msg) => `\x1b[31m${msg}\x1b[0m`; // Red
export const info = (msg) => `\x1b[34m${msg}\x1b[0m`; // Blue
export const warning = (msg) => `\x1b[33m${msg}\x1b[0m`; // Yellow
export const underline = (msg) => `\x1b[4m${msg}\x1b[0m`; // Underline
